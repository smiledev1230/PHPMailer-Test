<h3>What to Expect  &amp; What to Buy</h3>
<strong>The  Oculus Rift</strong><br>
  Come  demo the Oculus Rift - the 3D virtual reality headset that promise to change  the world and make you wealthy. If not at least it's a really cool experience  you can't have anywhere else. And if you don't throw up we'll give you some  free coffee and popcorn (we advise you at the popcorn after trying it out).</p>
<strong>SONOS </strong><br>
  Have  you heard of SONOS? Well it's only the original and best wireless hi-fi system  around, duh. Honest is a official SONOS Gold Dealer, help us get to Platinum  (if this happens soon we ill release an entire herd of unicorns back into the  wild). We created the SONOS Spot for you. Sit in our comfy makeshift SONOS  studio and listen to a SONOS home theater setup and tinker with app-based,  wireless speakers.</p>
<strong>Nest</strong><br>
  The  whole Nest line including the Nest Learning Thermostat, the Nest Protect (smart  smoke and CO detector) and the cloud based Dropcam will be making a cameo just  for you (yes, specifically for you). Come see and play with all the Nest  goodies. Sadly, Mr. Nest will not be attending this time.</p>
<strong>Lighting  Control</strong><br>
  Play  with the latest concepts in lighting control for single rooms small areas or  your entire home/business. Imagine controlling multiple lights and lamps all  with one touch on your phone, a wall keypad or remote. Or, in the dead of night  light up a pathway to the bathroom (or the fridge depending on what your  priorities are - were not gonna judge).</p>
<strong>Woofer  Wall</strong> - you all about dat bass?<br>
  Demo  and purchase some superbly powerful, yet subtly elegant subwoofers for your home  theater. Don't have a home theater? C'mon, don't worry we know how we can solve  that.* <br>
  <em>*P.S.  we will solve it by selling you one ;) - wink..</em>.</p>
<strong>Control</strong><strong> City</strong><strong> </strong><br>
  See  the best in universal remotes and whole-home control systems, ask questions and  see how Honest can make operating your home entertainment and home automation a  whole lot easier. Let's make you home somewhere you love to be (we've  trademarked that one already so just saying).</p>
<strong>Game  Garage</strong><br>
  Play  PS4 or Xbox One on a 100&quot; Black Diamond projection screen. The Black  Diamond screen by Screen Innovations features special light rejecting technology  that filters away unwanted light to allow you to view a brilliant picture no  matter if its day time or if you have the lights on in the room. Who loves some  Street Fighter? (we call dibs on Guile).</p>
<strong>Speaker-Ville</strong><br>
  Come  listen and see truly invisible speakers by Sonance (the Casper of speakers). Invisible speakers? Wait  a minute, what kind of black magic is that you say? No voo-doo here, just good  ole fashion innovation. The Sonance &quot;degrees of invisibility&quot; wall  will be on display to show off high-performance traditional in-wall/in-ceiling  speakers, stealth/vanishing grill speakers, the new discrete opening system and  of course the truly invisible line (oooh, spooky). Other brands on display  include Klipsch, Martin Logan, Denon, Onkyo and more...</p>
<strong>Smart  Home Automation</strong><br>
  We  will have many smart home concepts and new solutions out for tinkering with,  including: motorized digital window shades, lighting control, control systems,  universal remotes, smart thermostats and fire protection, security cameras,  app-based whole-home audio and a lot more.</p>
<strong>4K  Theater by JVC</strong><br>
  View  stunning 4K content (4K = Four x 1080) courtesy of JVC. We will have our 4K  theater setup to show-off the latest JVC projectors, some models not even available  in stores, for those looking for the best media room experiences. You'll start  crying after you see this stuff (Kleenex will be on hand).<strong></strong></p>
<strong>The  Great Outdoors</strong><br>
  Even  though it not summer anymore get ready for next season (c'mon it starts getting  warm here in Texas  around February people). We will have outdoor sound systems that make you feel  as if you're at a music festival or front row at the symphony. This space-age technology  is not available at retail stores and needs to be in your backyard pronto.<BR><BR>
<strong>Mirror  TV</strong><br>
  Yep  that's right, a mirror that turns into a TV with the push of a button, oh snap!  Need we say more? Okay here's a little more, Honest can create a custom Mirror  TV solution for the bathroom, bedroom or your living room. Actual Honest  technicians will be on hand to answer questions and talk more about TV, mirrors  and yep you guessed it: Mirror TV's.<BR><BR>
<strong>Yard  Sale</strong> - adopt an electronic device, save a life (not really)<strong></strong><br>
  We  collect our fair share of used electronics and our promise is to keep them out of  the land fill (we're not just yellow but also green too). We will have a section  dedicated to re-purposing said electronics. Try A/V Receivers and Stereos starting  at $35 and Large DLP TV's from $99! Even a VCR or two (true fact: someone asked  us the other day: what's a V-C-R, no really they did). So help the environment by  recycling these fine, gently-used electronics back into your domicile.</p>
<strong>Interactive  Touch Screen Display </strong>-  the digital white board<strong></strong><br>
  From  Newline Interactive a 65&quot; touch screen digital white board and display  will be on hand to demonstrate maximum productivity in a commercial or  educational environment. Also the their TruCast solution that allows you to  have multiple devices all collaborate wirelessly in a single conference room  will be there - trust us, it's a-w-e-s-o-m-e.</p>
<strong>Camera  Corner </strong>-  security &amp; CCTV<br>
  Learn  about analog and digital options available in the world of surveillance  cameras. We'll explain everything you need to catch you favorite burglar or  vandal in the act.</p>
<strong>High-Res  Audio by DEEZER<br>
</strong>High-res  ausio is here, experience Deezer. Okay imagine iTunes, Spotify and Pandora all  wrapped into one music service, sound good?. It is, Deezer is coming and they  have a catalog of 35 million songs and growing. The best part: it's all hi-res  which is way better than MP3 quality. For those who already have or will purchase  SONOS product, you may sign up for an exclusive FREE trial right at the show -  take that bother-in-law who thinks he gets all the cool stuff first.</p>
<strong>And  More...</strong><br>
  More  top secret stuff, you'll just have to show up to see. Bring your friends and  family but leave the in-laws at home or drop them off at a senior center with  ten bucks.<br><br>


