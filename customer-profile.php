"Since our room features double doors in the middle of the rear wall Honest 
chose to install two in-wall subwoofers, on each side in the back of the room in 
order to balance the low frequency sound. The dual in-wall subwoofers 
are awesome! You can hear every little detail. All those hard hits and tackles, 
when watching a game, remind me of the real thing. The bass sounds so smooth 
because they found a way to get rid of any wall rattle.
<br>
<br>

Honest also tied my lighting controls into the easy to use, touch-screen universal 
remote they gave me and so now all I have to do when starting a movie or 
when I'm done for the evening is press one button on the remote and all the 
equipment automatically starts up or winds down and all the lights fade off by 
themselves. Before I had several remotes to deal with and three or four lights
switches to manually operate.
<br>
<br>

I also love all the Apple integration they did for me so I can do the Airplay
features with my iPhone and my iTunes account, I can stream my music library to the media room wirelessly and order movies directly from iTunes right on 
the AppleTV screen. We also get Netflix in the highest quality through AppleTV.  
<br>
<br>

I spend so much time in the room my next improvement that Honest will tackle for me will be to help me with my home work and the ability to watch and 
review my game footage right on the projector instead of having to strain to see them on a small iPad screen." 
