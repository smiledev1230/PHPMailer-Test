The JVC projector Honest provided is top notch and whether in 2D or 
3D mode it is an awesome improvement over the old one. Honest 
recommended a special 'Black Diamond' Screen for us, together with the 
JVC Projector it makes for my very own home cinema experience.
<br>
<br>

The day I returned from training camp was the first time I saw the room fully
transformed, I immediately threw in the new 3D Batman game and I was 
blown away by the realism and richness coming from the Black Diamond 
Screen and the 3D projector. With this special screen I don't have to sit in 
the pitch-black to game anymore, I can actually have the lights on and still 
 

see the screen almost as good as with the lights off. The Black Diamond allows me to use the room with more flexibility. Before with my old setup I had to turn 
