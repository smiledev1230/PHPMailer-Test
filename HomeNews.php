      <P><STRONG><BR><FONT color=#f4b800 size=3>WHAT IS THE DIGITAL (DTV) 
      TRANSITION ALL ABOUT?</FONT></STRONG></P>
      <P>On <STRONG>February 17, 2009</STRONG> all full-power broadcast 
      television stations in the United States will stop broadcasting on analog 
      airwaves <BR>and begin broadcasting only in digital. Digital broadcasting 
      will allow stations to offer improved picture and sound quality as well 
      <BR>as additional channels.</P>
      <P><STRONG><FONT color=#f4b800 size=3>WILL I BE 
      AFFECTED?</FONT></STRONG></P>
      <P>If you use an antenna (in the U.S., such as rabbit ears or a larger 
      antenna on your roof) to watch TV on a set and don't subscribe to cable, 
      satellite or other pay TV service – you will need to upgrade to digital 
      television. If you have a Tube or “CRT” TV and don’t subscribe to cable or 
      satellite then you’ll need a Digital-to-Analog converter box.</P>
      <P><STRONG><FONT size=3>*</FONT>CABLE AND SATELLITE SUBSCRIBERS WILL NOT 
      BE AFFECTED, NO MATTER WHAT TYPE OF TV YOU CURRENTLY HAVE!</STRONG></P>
      <P><STRONG>Exceptions:<BR></STRONG><BR>1.&nbsp; The digital tuner rule 
      specified by congress says as of <STRONG>March 1, 2007</STRONG>, all new 
      TVs, VCRs, DVRs, ETC. must include digital tuners. This rule applies to 
      equipment <STRONG><U>MANUFACTURED</U></STRONG> after that date 
      <STRONG><U>ONLY</U></STRONG>. TVs made before this date can still be sold 
      without digital tuners.<BR><BR>2.&nbsp; Note that the transition only 
      applies to full-power TV stations. Some low-power stations will remain in 
      analog. (However most network affiliates are full-power)<BR><BR>3. If you 
      subscribe to cable/satellite and do not receive your local channels 
      through your cable/satellite box and switch off your receiver to get your 
      local channels then you will also be affected. Those that receive their 
      local channels though ther cable box are fine.</P>
