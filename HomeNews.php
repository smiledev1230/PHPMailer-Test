      <P><BR><FONT color=#f4b800><EM>An in-depth look&nbsp;at Television 
      now-a-days:</EM><BR></FONT><BR><FONT size=3><FONT color=#f4b800>ANALOG VS. 
      DIGITAL</FONT> <BR></FONT></STRONG><BR>Traditional analog TV has an aspect 
      ratio of 4:3 (almost square). This means that a TV screen is 4 inches wide 
      for every 3 inches it is tall. The reason for this is because when 
      broadcasting was created it was made for video-based cameras to broadcast 
      TV programs, news and not so much movies. This was before the time of 
      VCR’s, DVD Players, Pay TV (Such as Cable &amp; Satellite) and readily 
      available video rentals/sales.<BR><BR>Most digital televisions have an 
      aspect ratio of 16:9 (rectangular) and are called “widescreen.”&nbsp; This 
      is in part to accommodate content produced by film cameras such as motion 
      pictures (movies).<BR><BR>When the aspect ratio of the television program 
      and your television set do not match, bars on the top and/or sides of the 
      image may appear to ensure that the image is not distorted. In order to 
      properly display 16:9 content on a 4:3 set, your digital television or 
      digital-to-analog converter box may place bars at the top and bottom of 
      the image – a process called “letterboxing.”<BR><BR></P>
