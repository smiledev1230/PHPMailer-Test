      <P>The resolution of a TV is usually written out as "horizontal resolution 
      x vertical resolution." For example, a TV may have a resolution of 1386 x 
      788 pixels - 1386 pixels in the horizontal direction and 788 
      vertically.<BR><BR>As with the lines of resolution, you most commonly 
      refer to the vertical number of pixels and use that number when deciding 
      if you can really call a TV an HDTV. A TV must have at least 720 vertical 
      pixels to be called HDTV-capable. In the table above the Maximum 
      resolution refers to the vertical resolution (pixels). The “i” and “p” 
      next to the resolution will be explained in the next 
      section.<BR><BR><BR><STRONG><FONT color=#f4b800 size=3>PROGRESSIVE VS. 
      INTERLACED (i.e. 1080i vs. 1080p)</FONT></STRONG> <BR><BR>&nbsp;It’s time 
      to grab the duct tape. If your brain hasn’t already been overloaded, this 
      may put you over the edge. All analog programs are interlaced video but, 
      high definition video can be either interlaced “i” or progressively “p” 
      scanned. It's important to understand these terms.<BR><BR>Interlaced video 
      is scanned from side to side, top to bottom. Every 60th of a second, every 
      other line making up the complete picture is scanned. Then, a 60th of a 
      second later, the lines in-between the lines that have already appeared 
      are scanned. </P>
      <P>Effectively, half the picture's vertical resolution is sent in the 
      first 60th of a second, and the second half is sent in the second 60th of 
      a second. Both of these "halves" are displayed 30 times per second, which 
      makes 1 solid picture to the human eye. </P>
      <P>Progressive scan video is scanned from side to side, top to bottom: 
      line 1, line 2, line 3 etc.&nbsp; The full picture is displayed 60 times 
      per second, twice as many times as interlaced video.&nbsp; Progressively 
      scanning the video also helps reduce a flickering effect that you might 
      see out of interlaced video. <BR><BR>To sum this section up progressively 
      scanning video provides twice the picture as does an interlaced video with 
      the same number of lines of resolution. </P>
